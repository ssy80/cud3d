#include "../cub3D.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*cp;

	i = 0;
	cp = lst;
	while (cp != NULL)
	{
		i++;
		cp = cp->next;
	}
	return (i);
}
